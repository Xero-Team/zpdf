/// Built-in metrics for the 14 standard PDF fonts (PDF spec, Appendix D).
///
/// Width tables indexed by WinAnsiEncoding character code (0-255).
/// Values are in 1/1000 of a text space unit.

pub struct StandardFontMetrics {
    pub widths: &'static [u16; 256],
    pub ascent: f64,
    pub descent: f64,
    pub bbox: [f64; 4],
}

pub fn lookup(name: &str) -> Option<&'static StandardFontMetrics> {
    let canonical = normalize_name(name);
    STANDARD_FONTS
        .iter()
        .find(|(n, _)| *n == canonical)
        .map(|(_, m)| m)
}

fn normalize_name(name: &str) -> &str {
    match name {
        "Arial" => "Helvetica",
        "Arial,Bold" | "Arial-Bold" => "Helvetica-Bold",
        "Arial,Italic" | "Arial-Italic" => "Helvetica-Oblique",
        "Arial,BoldItalic" | "Arial-BoldItalic" => "Helvetica-BoldOblique",
        "TimesNewRoman" | "TimesNewRomanPS" => "Times-Roman",
        "TimesNewRoman,Bold" | "TimesNewRomanPS-Bold" => "Times-Bold",
        "TimesNewRoman,Italic" | "TimesNewRomanPS-Italic" => "Times-Italic",
        "TimesNewRoman,BoldItalic" | "TimesNewRomanPS-BoldItalic" => "Times-BoldItalic",
        "CourierNew" => "Courier",
        "CourierNew,Bold" | "CourierNew-Bold" => "Courier-Bold",
        "CourierNew,Italic" | "CourierNew-Italic" => "Courier-Oblique",
        "CourierNew,BoldItalic" | "CourierNew-BoldItalic" => "Courier-BoldOblique",
        other => other,
    }
}

